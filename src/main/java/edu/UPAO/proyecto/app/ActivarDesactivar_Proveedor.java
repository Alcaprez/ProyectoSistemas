    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ruc = jTextField2.getText().trim();
        String motivo = jTextField3.getText().trim();

        // Validaciones
        if (ruc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el RUC.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (motivo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un motivo.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!jRadioButton1.isSelected() && !jRadioButton2.isSelected()) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar Activar o Desactivar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Buscar proveedor
        Proveedor proveedor = proveedorService.buscarPorRuc(ruc);
        if (proveedor == null) {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ proveedor con RUC: " + ruc, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Determinar acci√≥n
        boolean activar = jRadioButton1.isSelected(); // true = Activar, false = Desactivar

        // Cambiar estado y registrar historial
        boolean exito = proveedorService.cambiarEstadoProveedorPorRuc(proveedor.getRuc(), activar, motivo);
        if (exito) {
            JOptionPane.showMessageDialog(this,
                    "Proveedor " + proveedor.getNombre() + " ha sido " + (activar ? "activado" : "desactivado")
                    + " con √©xito.\nMotivo: " + motivo,
                    "√âxito", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el estado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

