    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int id = Integer.parseInt(jTextField1.getText().trim()); // solo lectura
            String nombre = jTextField2.getText().trim();
            String telefono = jTextField3.getText().trim();
            String direccion = jTextField4.getText().trim();
            String ruc = jTextField6.getText().trim();
            String correo = jTextField7.getText().trim();
            String contacto = jTextField8.getText().trim();

            boolean exito = proveedorService.actualizarProveedorCampos(
                    id, nombre, ruc, telefono, correo, direccion, contacto);

            if (exito) {
                JOptionPane.showMessageDialog(this, "Proveedor actualizado con √©xito ");
                this.dispose(); // cerrar ventana
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ proveedor con el ID ingresado ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un n√∫mero v√°lido.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }    // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

