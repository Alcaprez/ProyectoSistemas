    private void registrarAsistencia(String tipo) {
        // Obtener fecha y hora actuales
        LocalDateTime ahora = LocalDateTime.now();
        DateTimeFormatter fechaFmt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        DateTimeFormatter horaFmt = DateTimeFormatter.ofPattern("HH:mm:ss");

        String fecha = ahora.format(fechaFmt);
        String hora = ahora.format(horaFmt);

        // Agregar a la tabla
        modelo.addRow(new Object[]{tipo, fecha, hora});

        // Guardar en archivo de texto
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("asistencias.txt", true))) {
            bw.write(tipo + " | " + fecha + " | " + hora);
            bw.newLine();
        } catch (IOException ex) {
            ex.printStackTrace();
        }

    }
