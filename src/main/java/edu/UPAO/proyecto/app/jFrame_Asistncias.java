    private void cargarAsistencias() {
        try (java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader("asistencias.txt"))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                // separar por el separador " | "
                String[] partes = linea.split("\\|");
                if (partes.length == 3) {
                    String tipo = partes[0].trim();
                    String fecha = partes[1].trim();
                    String hora = partes[2].trim();
                    modelo.addRow(new Object[]{tipo, fecha, hora});
                }
            }
        } catch (IOException ex) {
            // si no existe el archivo, no pasa nada
            System.out.println("No hay registros previos.");
        }
    }

