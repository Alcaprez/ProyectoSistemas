    private void btn_validarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_validarActionPerformed
        String cupon = txtCupon.getText().trim();

        if (cupon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un cupÃ³n primero.");
            return;
        }

        PromocionController promoCtrl = new PromocionController();
        double subtotal = calcularSubtotalCarrito();

        double totalConCupon = promoCtrl.aplicarCupon(cupon, subtotal);

        if (totalConCupon < subtotal) {
            JOptionPane.showMessageDialog(this, "CupÃ³n vÃ¡lido: 10% de descuento aplicado.");
            lbl_descuento.setText("CupÃ³n aplicado: 10%");
            lblTotal.setText(String.format("S/ %.2f", totalConCupon));
        } else {
            JOptionPane.showMessageDialog(this, "CupÃ³n invÃ¡lido o caducado.");
            lbl_descuento.setText("Sin descuento");
            lblTotal.setText(String.format("S/ %.2f", subtotal));
        }
    }//GEN-LAST:event_btn_validarActionPerformed

