    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        int filaSeleccionada = tablaProductos.getSelectedRow();
        if (filaSeleccionada != -1) {

            String nombre = tablaProductos.getValueAt(filaSeleccionada, 0).toString();
            double precio = Double.parseDouble(tablaProductos.getValueAt(filaSeleccionada, 1).toString());
            int stock = Integer.parseInt(tablaProductos.getValueAt(filaSeleccionada, 2).toString());
            String codigo = tablaProductos.getValueAt(filaSeleccionada, 3).toString();

            int cantidad = (Integer) spCantidad.getValue();

            if (cantidad > stock) {
                javax.swing.JOptionPane.showMessageDialog(this, "No hay suficiente stock.");
                return;
            }

            double subtotal = precio * cantidad;

            DefaultTableModel modeloCarrito = (DefaultTableModel) miniTabla.getModel();
            Object[] fila = {nombre, cantidad, subtotal};
            modeloCarrito.addRow(fila);

            actualizarTotal();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un producto primero.");
        }
    }//GEN-LAST:event_btn_agregarActionPerformed

