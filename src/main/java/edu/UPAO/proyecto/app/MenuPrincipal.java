    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        String textoBusqueda = txtBuscarCodigo.getText().trim();
        if (textoBusqueda.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un cÃ³digo o nombre para buscar.");
            return;
        }

        ProductoController pc = new ProductoController();
        List<Producto> productos = pc.cargarProductos();

        DefaultTableModel modelo = (DefaultTableModel) tablaProductos.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        boolean encontrado = false;
        for (Producto p : productos) {
            if (p.getCodigo().equalsIgnoreCase(textoBusqueda)
                    || p.getNombre().equalsIgnoreCase(textoBusqueda)) {
                modelo.addRow(new Object[]{p.getNombre(), p.getPrecioVenta(), p.getStock(), p.getCodigo(), p.getVendidos()});
                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Producto no encontrado.");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_actualizarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarItemActionPerformed
        int fila = miniTabla.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un producto para actualizar.");
            return;
        }

        int nuevaCantidad = (int) sp_item.getValue();
        if (nuevaCantidad <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor a 0.");
            return;
        }

        DefaultTableModel modelo = (DefaultTableModel) miniTabla.getModel();
        double precioUnitario = Double.parseDouble(modelo.getValueAt(fila, 2).toString());

        // actualizar cantidad y subtotal
        modelo.setValueAt(nuevaCantidad, fila, 1);
        modelo.setValueAt(precioUnitario * nuevaCantidad, fila, 2);

        actualizarTotal(); // recalcula el total despuÃ©s de actualizar
    }//GEN-LAST:event_btn_actualizarItemActionPerformed

